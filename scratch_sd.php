<h4>6. Itihasas: Epics</h4>
<p>Though Itihaas in Sanskrit means history but in religious context Itihasas tell the philosophical and ethical stories of divine incarnations. They include the epics of Ramayana and Mahabharata.</p>
<p><span style="color: orangered">Ramayana:</span> Ramayana was composed by Rishi Valmiki around 200 years BCE. Ramayana tells the epic story of Lord Rama, incarnation of Lord Vishnu.</p>
<p><span style="color: orangered">Mahabharata:</span> Mahabharata was composed by sage Vyasa. This epic story is on Dharma in particular of Dharma of kingship. Mahabharata is well known for Bhagavad Gita in which message of Lord Krishna,<br />
incarnation of Lord Vishnu, is stated.</p>
<p><!-- Dharam Shastras --></p>
<h4>7. Dharam Shastras</h4>
<p>Dharam Shastras are texts / documents / books / manuals that contain laws about morality and religious duties. They also provide guiding rules and principles for the righteous conduct and regularity of society at large.</p>
<p>Dharma taught in Dharam Shastras is based on customs and pronouncements of the writers of these texts that were likely derived from evolving ethical / ideological / cultural / legal practices. <b>It has little to do with Vedas and the word Dharam Shastra never appears in Vedic texts.</b></p>
<p>Dharam Shastras are not written by a single author and these have been reviewed and commented upon in ancient and medieval era.<b> Dharam Shastras are inconsistent with each other raising questions to their authenticity.</b></p>
<p>Main Dharam Shastras are:</p>
<ol class="sastra_list" type="a">
<li>Manu Smriti</li>
<li>Yajnayaka Smriti</li>
<li>Narada Smriti</li>
<li>Vishnu Smriti</li>
<li>Brhaspati Smriti</li>
<li>Katayana Smriti</li>
</ol>
<p><b><br />
Dharam Shastras are based on ancient Dharam Sutra texts.</b></p>
<p>Dharam sutras are guidelines for individual and social behavior. Dharma in Dharam sutras is diverse and includes;</p>
<ul class="sastra_list">
<li>Accepted norms of behaviour</li>
<li>Procedures within a ritual</li>
<li>Moral actions</li>
<li>Righteousness and ethical attitudes</li>
<li>Civil and criminal law</li>
<li>Legal procedures</li>
<li>Penance and punishment</li>
<li>Guidelines for proper and productive living</li>
<li>Social institutions i.e., marriage, inheritance, adoption, work contracts, judicial process, meals and food intake, sexual conduct etc.</li>
</ul>
<p><b><br />
These Dharmas are not found in Vedic texts nor the behavioural rules found in any of the Vedas.</b></p>
<p>Dharam Sutras are numerous in number but only few have survived.</p>
